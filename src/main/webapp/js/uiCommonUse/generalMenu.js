function setLayoutDiv() {
    layoutAttributes.style.display = "block";
    contentAttributes.style.display = "none";
    tabLayout.style.background = "#E7F7FF";
    tabContent.style.background = "#0000";
//    scrLayoutAttr.scroll_y.resize(scrLayoutAttr);
}

function setContentDiv() {
    layoutAttributes.style.display = "none";
    contentAttributes.style.display = "block";
    tabContent.style.background = "#E7F7FF";
    tabLayout.style.background = "#0000";
}

